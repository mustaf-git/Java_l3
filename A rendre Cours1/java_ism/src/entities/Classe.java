package entities;

/**
 *
 * @author DELL
 */
public class Classe {
    private int id;
    private String libelle;

    public Classe() {
    }

    public Classe(String libelle) {
        this.libelle = libelle;
    }

    public Classe(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLibelle() {
        return libelle;
    }

    public void setLibelle(String libelle) {
        this.libelle = libelle;
    }
    
}
